#SLICING 
lst=[1,2,3,4,5]
print(lst[1:4])
#APPENDING
lst.append(6)
#FINDING INDEX
print(lst.index(3))
#SORTING
lst.sort()
#POPPING
lst.pop()
#REMOVING
lst.remove(2)
#INSERTING
lst.insert(1,7)
#COUNT OCCURRENCES
print(lst.count(7))
#EXTENDING
lst.extend([8,9])
#REVERSE
lst.reverse()
print(lst)
